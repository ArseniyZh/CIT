# test_smallest_in_longest_positive_sequence.py

import pytest
from smallest_in_longest_positive_sequence import smallest_in_longest_positive_sequence

@pytest.mark.parametrize(
    "N, A, expected",
    [
        # Тестовый вариант 1: Все элементы положительные
        (5, [3, 2, 5, 4, 1], 1),

        # Тестовый вариант 2: Все элементы неположительные
        (4, [-1, 0, -2, -3], None),

        # Тестовый вариант 3: Одна непрерывная последовательность положительных элементов
        (6, [-1, 2, 3, 4, -2, -3], 2),

        # Тестовый вариант 4: Несколько последовательностей разной длины
        (10, [1, 2, -1, 3, 4, 5, -2, 6, 7, -3], 3),

        # Тестовый вариант 5: Несколько последовательностей одинаковой максимальной длины
        (8, [1, 2, -1, 3, 4, -2, 5, 6], 1),

        # Тестовый вариант 6: Массив начинается и заканчивается положительными элементами
        (9, [3, 4, -1, 2, 3, -2, 4, 5, 6], 4),  # Исправлено с 2 на 4

        # Тестовый вариант 7: Массив содержит только один положительный элемент
        (5, [-1, -2, 5, -3, -4], 5),

        # Тестовый вариант 8: Массив содержит положительные и нулевые элементы
        (7, [0, 1, 2, 0, 3, 4, 0], 1),

        # Дополнительные тесты

        # Тестовый вариант 9: Пустой массив
        (0, [], None),

        # Тестовый вариант 10: Все элементы равны нулю
        (4, [0, 0, 0, 0], None),

        # Тестовый вариант 11: Последовательности с одинаковыми min_element
        (7, [1, 3, -1, 1, 3, -2, 1], 1),

        # Тестовый вариант 12: Последовательности с различными min_element
        (10, [2, 4, -1, 1, 3, 5, -2, 0, 1, 2], 1),
    ]
)
def test_smallest_in_longest_positive_sequence(N, A, expected):
    assert smallest_in_longest_positive_sequence(N, A) == expected
