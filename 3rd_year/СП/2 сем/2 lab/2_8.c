#include <stdio.h>
#include <stdlib.h>
#include <time.h>

#define SIZE 10

int main() {
    int A[SIZE][SIZE];
    int sum = 0; // Сумма отрицательных чисел

    srand(time(NULL)); // Инициализация генератора случайных чисел

    // Заполняем массив случайными числами от -50 до 50
    printf("Массив A:\n");
    for (int i = 0; i < SIZE; i++) {
        for (int j = 0; j < SIZE; j++) {
            A[i][j] = rand() % 101 - 50; // Генерируем числа от -50 до 50
            printf("%4d ", A[i][j]); // Вывод массива
        }
        printf("\n");
    }

    // Проходим по нижнему треугольнику (включая главную диагональ)
    for (int i = 0; i < SIZE; i++) {
        for (int j = 0; j <= i; j++) { // j ≤ i означает область ниже или на диагонали
            if (A[i][j] < 0) { // Проверяем, является ли элемент отрицательным
                sum += A[i][j];
            }
        }
    }

    printf("\nСумма отрицательных элементов в незакрашенной области: %d\n", sum);

    return 0;
}
